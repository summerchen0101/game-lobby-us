import { readFileSync, mkdirSync, writeFileSync } from 'node:fs'
import { dirname, join } from 'node:path'
import { fileURLToPath } from 'node:url'
import protobuf from 'protobufjs'

const __dirname = dirname(fileURLToPath(import.meta.url))
const webRoot = join(__dirname, '..')
const protoPath = join(webRoot, 'proto/gateway_wire.proto')
const proto = readFileSync(protoPath, 'utf8')
const root = protobuf.parse(proto).root
const json = root.toJSON()

const outDir = join(webRoot, 'src/gen')
mkdirSync(outDir, { recursive: true })
const tsPath = join(outDir, 'gateway_wire.schema.ts')
writeFileSync(
  tsPath,
  `// Generated by scripts/generate-gateway-schema.mjs — do not edit by hand.\n` +
    `export default ${JSON.stringify(json, null, 2)} as const\n`,
)
console.log('Wrote', tsPath)
